        const sections = document.querySelectorAll(".section");
        
        // El "punto de gatillo" será la mitad de la pantalla, no la parte superior.
        // Esto hace que el cambio sea más natural mientras bajas.
        const triggerPoint = window.scrollY + (window.innerHeight / 2);

        sections.forEach((section, index) => {
            const sectionTop = section.offsetTop;
            const sectionHeight = section.offsetHeight;

            // Si el punto medio de la pantalla está DENTRO de la sección actual
            if (triggerPoint >= sectionTop && triggerPoint < sectionTop + sectionHeight) {
                switch (index) {
                    case 0: // HISTORIA
                        document.body.style.backgroundImage = "url('./images/nosotros/h2.jpeg')";
                        break;
                    case 1: // MISIÓN
