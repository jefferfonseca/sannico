    } else if (footerTop <= viewportHeight + toc.offsetHeight) {
        toc.style.top = `${maxTop}px`; // Mantiene el TOC justo arriba del footer
    } else {
        toc.style.top = "140px"; // Mantiene una posición fija
    }
